package main

import "fmt"

func main() {
	// string
	var name string = "GoLang"
	// without datatype declaration
	var name2 = "DevOps"

	// int
	var num int = 34
	var num2 = 33

	// boolean
	var is_true bool = true
	var is_false bool = false

	// shorthand variable 
	name3 := "golang"

	fmt.Println(name)
	fmt.Println(name2)
	fmt.Println(name3)
	fmt.Println(num)
	fmt.Println(num2)
	fmt.Println(is_true)
	fmt.Println(is_false)
}